/**
 * Complete the function getLastCharacter such that it returns the last character of the name parameter it recieves.
 * 
 * @param {string} name
 */
function getLastCharacter(name) {
    return name[name.length - 1];
}

// Sample usage - do not modify
console.log(getLastCharacter("Sam"));
console.log(getLastCharacter("Alex"));
console.log(getLastCharacter("Charley"));