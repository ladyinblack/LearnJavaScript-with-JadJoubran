/**
 * Complete the function getCharCount such that it returns the number of characters in the str parameter that it receives.
 * 
 * @param {string} str 
 */
function getCharCount(str) {
    return str.length;
}

// Sample usage - do not modify
console.log(getCharCount("Sam"));
console.log(getCharCount("Alex 123"));
console.log(getCharCount("Charley is here"));